CREATE TABLE local_authority (
     id                 INTEGER         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
     created_date       TIMESTAMPTZ(6)  DEFAULT current_timestamp NOT NULL,
     last_modified_date TIMESTAMPTZ(6),
     name               VARCHAR(255)    NOT NULL,
     custodian_code     VARCHAR(255)    NOT NULL UNIQUE
);

CREATE TABLE address (
    id                  BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_date        TIMESTAMPTZ(6)  DEFAULT current_timestamp NOT NULL,
    last_modified_date  TIMESTAMPTZ(6),
    uprn                BIGINT          UNIQUE,
    single_line_address VARCHAR(1000)   NOT NULL,
    organisation        VARCHAR(255),
    sub_building        VARCHAR(500),
    building_name       VARCHAR(255),
    building_number     VARCHAR(255),
    street_name         VARCHAR(255),
    locality            VARCHAR(255),
    town_name           VARCHAR(255),
    postcode            VARCHAR(255)    NOT NULL,
    local_authority_id  INTEGER,
    is_active           BOOLEAN         DEFAULT true NOT NULL,
    FOREIGN KEY (local_authority_id) REFERENCES local_authority
);

COMMENT ON TABLE address IS 'dataPackageVersionId=';

